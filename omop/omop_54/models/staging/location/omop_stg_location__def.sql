
select 
     cast(null as varchar(50)) as address_1
    ,cast(null as varchar(50)) as address_2
    ,cast(null as varchar(50)) as city
    ,cast(null as varchar(2)) as state
    ,cast(null as varchar(9)) as zip
    ,cast(null as varchar(20)) as county
    ,cast(null as varchar(50)) as location_source_value
    ,cast(null as int) as country_concept_id
    ,cast(null as varchar(80)) as country_source_value
    ,cast(null as float) as latitude    -- Must be between -90 and 90
    ,cast(null as float) as longitude   -- Must be between -180 and 180

where 1=0





    