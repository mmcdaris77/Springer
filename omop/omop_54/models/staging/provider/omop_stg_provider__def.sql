
select 
    cast(null as int) as provider_id
    ,cast(null as varchar(255)) as provider_name
    ,cast(null as varchar(20)) as npi
    ,cast(null as varchar(20)) as dea
    ,cast(null as int) as specialty_concept_id
    ,cast(null as int) as care_site_id
    ,cast(null as int) as year_of_birth
    ,cast(null as int) as gender_concept_id
    ,cast(null as varchar(50)) as provider_source_value
    ,cast(null as varchar(50)) as specialty_source_value
    ,cast(null as int) as specialty_source_concept_id
    ,cast(null as varchar(50)) as gender_source_value
    ,cast(null as int) as gender_source_concept_id

where 1=0



    